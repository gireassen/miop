import networkx as nx
import matplotlib.pyplot as plt

matrix = [
    '0111111110101100010101000000000000',
    '1011000100000100010101000000000000',
    '1101000110000100000000000000000010',
    '1110000100001100000000000000000000',
    '1000001000100000000000000000000000',
    '1000001000100000100000000000000000',
    '1000110000000000100000000000000000',
    '1111000000000000000000000000000000',
    '1010000000000000000000000000001011',
    '0000000000000000000000000000000000',
    '1000110000000000000000000000000000',
    '0000000000000000000000000000000000',
    '1001000000000000000000000000000000',
    '1111000000000000000000000000000000',
    '0000000000000000000000000000000011',
    '0000000000000000000000000000000011',
    '0000011000000000000000000000000000',
    '1100000000000000000000000000000000',
    '0000000000000000000000000000000011',
    '1100000000000000000000000000000000',
    '0000000000000000000000000000000011',
    '1100000000000000000000000000000000',
    '0000000000000000000000000000000011',
    '0000000000000000000000000001010011',
    '0000000000000000000000000100000100',
    '0000000000000000000000001000000100',
    '0000000000000000000000000000010001',
    '0000000000000000000000010000000001',
    '0000000000000000000000000000000101',
    '0000000000000000000000010010000011',
    '0000000010000000000000000000000011',
    '0000000000000000000000001100100011',
    '0010000010000011001010110000011101',
    '0000000010000011001010110011111110'
]

G = nx.Graph()

# Добавляем вершины в граф
num_vertices = len(matrix)
G.add_nodes_from(range(num_vertices))

# Добавляем ребра в граф
for i in range(num_vertices):
    for j in range(num_vertices):
        if matrix[i][j] == "1":
            G.add_edge(i, j)

# Рисуем граф
pos = nx.spring_layout(G)  # Определение позиций вершин для отображения
nx.draw(G, pos, with_labels=True, node_color='skyblue', node_size=500, edge_color='gray')

# Настройка отображения графа
plt.title("Граф связности")
plt.axis('off')

# Вывод изображения графа
plt.show()
